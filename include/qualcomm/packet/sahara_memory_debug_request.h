#pragma once

#include "qualcomm/packet/sahara_packet.h"
#include "qualcomm/packet/sahara_memory_read_request.h"

using OpenPST::Qualcomm::SaharaPacket;
using OpenPST::Qualcomm::SaharaMemoryReadRequest;

namespace OpenPST {
    namespace Qualcomm {
        
        class SaharaMemoryDebugRequest : public SaharaPacket
        {
            public:
                /**
                * @brief Constructor
                */
                SaharaMemoryDebugRequest(PacketEndianess targetEndian = kPacketEndianessLittle);
                
                /**
                * @brief Destructor
                */
                ~SaharaMemoryDebugRequest();

                /**
                * @brief Get address
                * @return uint32_t
                */
                uint32_t getAddress();
                
                /**
                * @brief Set address
                * @param uint32_t address
                * @return void
                */
                void setAddress(uint32_t address);
                /**
                * @brief Get size
                * @return uint32_t
                */
                uint32_t getSize();
                
                /**
                * @brief Set size
                * @param uint32_t size
                * @return void
                */
                void setSize(uint32_t size);
				/**
				* @overload Packet::unpack
				*/
	            void unpack(std::vector<uint8_t>& data, TransportInterface* transport) override;

				/**
				* @overload Packet::prepareResponse
				*/
				void prepareResponse() override;

        };
    }
}
